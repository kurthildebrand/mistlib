static void hexdump(const void* data, unsigned len, unsigned linelen)
{
	const uint8_t* ptr = data;

	unsigned i,j;

	for(i = 0; i < len; i += linelen)
	{
		/* Address */
		printf("%04X ", i);

		/* One line of bytes */
		for(j = 0; j < linelen; j++)
		{
			if(i + j >= len) {
				printf("   ");
			} else {
				printf("%02X ", ptr[i+j]);
			}
		}

		/* Character representation of bytes */
		for(j = 0; j < linelen; j++)
		{
			if(i + j >= len) {
				break;
			} else if(ptr[i+j] < ' ' || ptr[i+j] > '~') {
				printf(".");
			} else {
				printf("%c", ptr[i+j]);
			}
		}

		printf("\n");
	}
}


